void solve()
{
    int n;
    cin >> n;
    vector<int> arr;
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        arr.push_back(x);
    }
    int min = arr[0];
    int sum = 0;
    for (int i = 0; i < n; i++)
    {
        if (min > arr[i])
            min = arr[i];

        sum += arr[i];
    }
    int nas = sum - min;
    cout << nas << endl;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
}